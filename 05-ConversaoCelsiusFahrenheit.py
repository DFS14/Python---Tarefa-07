#Exercício: Conversão de graus Celsius em Fahrenheit de 10 em 10 graus

celsius = 10
while celsius <= 100:
    fahrenheit = (celsius * 9/5) + 32    
    print(celsius,"°C é igual", fahrenheit, "°F") 
    
    celsius += 10
    

